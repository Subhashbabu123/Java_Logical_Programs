import java.util.Scanner;
class NUMBER_FOR
  {
  public static void main(String args[])
    {
    int i;
    int n;
      Scanner sc=new Scanner(System.in);
      System.out.println("enter n value");
      n=sc.nextInt();
      
    for(i=1;i<=n;i++)
      {
        System.out.println(i);
      }
    }
  }